#面向对象：事物比较复杂，使用简单的线性思维无法解决
#共同点：都是解决实际问题的一种思维方式。二者相辅相成，并不是对立的。解决复杂问题，通过面向对象方式便于我们从宏观上
#把握事物之间复杂的关系、方便我们分析整个系统；具体到微观操作，仍然使用面向过程方式来处理

